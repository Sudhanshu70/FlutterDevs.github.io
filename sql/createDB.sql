ALTER TABLE `comments` ADD `COMMENT_CONTENT` TEXT NOT NULL AFTER `COMMENT_EMAIL`,
ADD `COMMENT_STATUS` VARCHAR(244) NOT NULL AFTER `COMMENT_CONTENT`,
