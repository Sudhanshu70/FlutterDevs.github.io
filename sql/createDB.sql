ADD `post_author` VARCHAR(244) NOT NULL AFTER `post_title`,
ADD `post_date` DATE NOT NULL AFTER `post_author`,
ADD `post_image` VARCHAR(244) NOT NULL AFTER `post_date`,
ADD `post_content` TEXT NOT NULL AFTER `post_image`,
ADD `post_category` VARCHAR(244) NOT NULL AFTER `post_content`,
