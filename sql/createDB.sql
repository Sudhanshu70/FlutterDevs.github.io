ADD `COMMENT_POST_TITLE` VARCHAR(244) NOT NULL AFTER `COMMENT_DATE`,
ADD `COMMENT_TAG` VARCHAR(100) NOT NULL AFTER `COMMENT_POST_TITLE`;


CREATE TABLE `sudhanshu_php_blog`.`users`
( `USER_ID` INT(30) NOT NULL AUTO_INCREMENT ,
