CREATE TABLE `sudhanshu_php_blog`.`posts`
( `post_id` INT(100) NOT NULL AUTO_INCREMENT ,
`post_tags` VARCHAR(244) NOT NULL ,
`post_comment_count` INT(100) NOT NULL ,
`post_status` VARCHAR(244) NOT NULL ,
PRIMARY KEY (`post_id`)) ENGINE = InnoDB;

ALTER TABLE `posts`
